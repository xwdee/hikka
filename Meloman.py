from .. import loader, utils
from hikkatl.tl.types import InputMessagesFilterMusic
import random

@loader.tds
class Meloman(loader.Module):
    """–ú–æ–¥—É–ª—å –¥–ª—è –≤—ñ–¥–ø—Ä–∞–≤–∫–∏ –≤–∏–ø–∞–¥–∫–æ–≤–∏—Ö –ø—ñ—Å–µ–Ω—å —É —á–∞—Ç"""

    strings = {
        "name": "MelomanMod",
        "no_songs": "<b>üéµ –ù–µ–º–∞—î –¥–æ—Å—Ç—É–ø–Ω–∏—Ö –ø—ñ—Å–µ–Ω—å —É –≤–∫–∞–∑–∞–Ω–∏—Ö —á–∞—Ç–∞—Ö.</b>",
        "song_sent": "<b>üéµ –í—ñ–¥–ø—Ä–∞–≤–ª–µ–Ω–æ –≤–∏–ø–∞–¥–∫–æ–≤—É –ø—ñ—Å–Ω—é!</b>",
        "invalid_chat_id": "<b>‚ùå –ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω–∏–π ID —á–∞—Ç—É –≤ –∫–æ–Ω—Ñ—ñ–≥—É.</b>",
    }

    def __init__(self):
        self.config = loader.ModuleConfig(
            loader.ConfigValue(
                "sources",
                [],
                "–°–ø–∏—Å–æ–∫ ID —á–∞—Ç—ñ–≤, –∑–≤—ñ–¥–∫–∏ –±—Ä–∞—Ç–∏ –ø—ñ—Å–Ω—ñ (ID –º–∞—é—Ç—å –ø–æ—á–∏–Ω–∞—Ç–∏—Å—è –∑ -100), –¥–æ–±–∞–≤–ª—è—é—á–∏ —á–µ—Ä–µ–∑ ¬´,¬ª",
            ),
        )

    async def rmscmd(self, message):
        """–í—ñ–¥–ø—Ä–∞–≤–∏—Ç–∏ –≤–∏–ø–∞–¥–∫–æ–≤—É –ø—ñ—Å–Ω—é"""
        sources = self.config["sources"]

        if not isinstance(sources, list):
            sources = [sources]

        if not all(isinstance(chat_id, int) and str(chat_id).startswith("-100") for chat_id in sources):
            return await utils.answer(message, self.strings("invalid_chat_id"))

        if not sources:
            return await utils.answer(
                message, "<b>üéµ –í–∏ –Ω–µ –≤–∫–∞–∑–∞–ª–∏ –¥–∂–µ—Ä–µ–ª–æ –ø—ñ—Å–µ–Ω—å —É –∫–æ–Ω—Ñ—ñ–≥—É.</b>"
            )

        audio_messages = []
        for chat_id in sources:
            try:
                async for msg in self.client.iter_messages(chat_id, filter=InputMessagesFilterMusic()):
                    if msg.audio:
                        audio_messages.append(msg)
            except Exception as e:
                await utils.answer(
                    message,
                    f"<b>‚ùå –ù–µ –≤–¥–∞–ª–æ—Å—è –æ—Ç—Ä–∏–º–∞—Ç–∏ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –∑ —á–∞—Ç—É {chat_id}: {e}</b>",
                )
                continue

        if not audio_messages:
            return await utils.answer(message, self.strings("no_songs"))

        random_song = random.choice(audio_messages)
        await random_song.forward_to(message.chat_id)
        await utils.answer(message, self.strings("song_sent"))
