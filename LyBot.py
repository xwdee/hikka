from telethon.tl.types import Message

from .. import loader, utils


@loader.tds
class MusicFinder(loader.Module):
    """Simple module to find music via bot"""

    strings = {
        "name": "Music Finder",
        "no_text": (
            "<emoji document_id=5312526098750252863>üö´</emoji> <b>Provide a name to find music with</b>"
        ),
        "processing": (
            "<emoji document_id=5451646226975955576>‚åõÔ∏è</emoji> <b>Processing...</b>"
        ),
    }

    strings_ru = {
        "name": "–ò—Å–∫–∞—Ç–µ–ª—å –ü–µ—Å–µ–Ω",
        "no_text": (
            "<emoji document_id=5312526098750252863>üö´</emoji> <b>–£–∫–∞–∂–∏ –Ω–∞–∑–≤–∞–Ω–∏–µ –¥–ª—è –ø–æ–∏—Å–∫–∞ –ø–µ—Å–Ω–∏</b>"
        ),
        "processing": (
            "<emoji document_id=5451646226975955576>‚åõÔ∏è</emoji> <b>–û–±—Ä–∞–±–æ—Ç–∫–∞...</b>"
        ),
        "_cmd_doc_aniq": "<–∑–∞–ø—Ä–æ—Å> - –ü–æ–∏—Å–∫ –ø–µ—Å–µ–Ω —á–µ—Ä–µ–∑ @LyBot",
        "_cls_doc": "–ü—Ä–æ—Å—Ç–µ–Ω—å–∫–∏–π –º–æ–¥—É–ª—å –ø–æ –ø–æ–∏—Å–∫—É –ø–µ—Å–µ–Ω",
    }

    async def mfindcmd(self, message: Message):
        """<request> - Send music with @LyBot"""
        args = utils.get_args_raw(message)
        if not args:
            await utils.answer(message, self.strings("no_text"))
            return

        message = await utils.answer(message, self.strings("processing"))

        try:
            query = await self._client.inline_query("@LyBot", args)
            await message.respond(file=query[0].document)
        except Exception as e:
            await utils.answer(message, str(e))
            return

        if message.out:
            await message.delete()
